"""
Generate docs/packs.html, the browsable skill-reference page (RR-B-30), from SKILLS.md.

The page is the single source's downstream artifact: it parses the SKILLS.md table
(slug, source licence, description) and the version from RELEASE-INFO.txt, then emits a
self-contained, design-system-compliant HTML page with a client-side filter box. No
third-party assets, no CDN. Regenerate after any pack/SKILLS.md change so it cannot drift
(RR-B-00). Em dashes are stripped from emitted copy (RR-B-28).
"""
import re
import sys
import html
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent


def deslop(text: str) -> str:
    """Remove em dashes from emitted copy (RR-B-28). Replace with a comma + space,
    collapsing any surrounding spaces so ' — ' becomes ', '."""
    text = re.sub(r"\s*—\s*", ", ", text)
    return text


def parse_skills() -> list[dict]:
    md = (ROOT / "SKILLS.md").read_text(encoding="utf-8")
    rows = []
    for m in re.finditer(r"^\|\s*\[`([^`]+)`\]\(([^)]+)\)\s*\|\s*([^|]+?)\s*\|\s*(.+?)\s*\|\s*$", md, re.M):
        slug, path, licence, desc = m.groups()
        rows.append({
            "slug": slug.strip(),
            "path": path.strip(),
            "licence": deslop(licence.strip()),
            "desc": deslop(desc.strip()),
        })
    return rows


def version() -> str:
    ri = (ROOT / "RELEASE-INFO.txt").read_text(encoding="utf-8")
    m = re.search(r"Version:\s*([0-9]+\.[0-9]+\.[0-9]+)", ri)
    return m.group(1) if m else "0.0.0"


REPO = "https://github.com/jgsystemsconsulting/jgs-se-knowledge-packs"
PAGES = "https://jgsystemsconsulting.github.io/jgs-se-knowledge-packs"


def render(rows: list[dict], ver: str) -> str:
    n_content = len(rows)
    cells = []
    for r in rows:
        slug = html.escape(r["slug"])
        licence = html.escape(r["licence"])
        desc = html.escape(r["desc"])
        href = f"{REPO}/blob/main/{r['path']}"
        # data-text powers the client-side filter (slug + licence + description, lowercased)
        data = html.escape(f"{r['slug']} {r['licence']} {r['desc']}".lower(), quote=True)
        cells.append(
            f'<tr data-text="{data}">'
            f'<td class="slug"><a href="{href}">{slug}</a></td>'
            f'<td class="lic">{licence}</td>'
            f'<td class="desc">{desc}</td></tr>'
        )
    rows_html = "\n".join(cells)


def main() -> int:
    rows = parse_skills()
    if not rows:
        print("ERROR: no pack rows parsed from SKILLS.md", file=sys.stderr)
        return 1
    out = ROOT / "docs" / "packs.html"
    out.write_text(render(rows, version()), encoding="utf-8")
    print(f"wrote {out.relative_to(ROOT)} ({len(rows)} packs)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
